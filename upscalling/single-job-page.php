                            A break-even analysis predicts the sales volume, at a given price, required to recover total costs. In other words, it’s the sales level that is the dividing line between operating at a loss and operating at a profit. Include all assumptions upon which your break-even calculation is based. A break even point tells you the number of units you must sell to make sure that your business does not make a loss. If you sell less than the break even number of units you will make a loss. You start making a profit when you sell more units than the break even number of units. The formula for break even quantity is:
